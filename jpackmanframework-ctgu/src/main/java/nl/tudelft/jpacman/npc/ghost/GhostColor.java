package nl.tudelft.jpacman.npc.ghost;

/**
 * 表示Pac-Man游戏中幽灵的颜色枚举。
 * <p>
 * 每个枚举常量对应一种特定颜色的幽灵及其别名。
 *
 * @author Jeroen Roosen
 * @since 1.0
 */
public enum GhostColor {
    /**
     * 红色幽灵，别名“Shadow”或“Blinky”。
     */
    RED,

    /**
     * 青色幽灵，别名“Bashful”或“Inky”。
     */
    CYAN,

    /**
     * 粉色幽灵，别名“Speedy”或“Pinky”。
     */
    PINK,

    /**
     * 橙色幽灵，别名“Pokey”或“Clyde”。
     */
    ORANGE
}
